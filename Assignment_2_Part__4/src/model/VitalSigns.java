package model;

/**
 *
 * @author khushbu
 */

public class VitalSigns {

    private double respiratoryRate;
    private double heartRate;
    private double bloodPressure;
    private double weightInKilos;
    private double weightInPounds;
    private Boolean isRecordNormal;

    public double getRespiratoryRate() {
        return respiratoryRate;
    }

    public void setRespiratoryRate(double respiratoryRate) {
        this.respiratoryRate = respiratoryRate;
    }

    public double getHeartRate() {
        return heartRate;
    }

    public void setHeartRate(double heartRate) {
        this.heartRate = heartRate;
    }

    public double getBloodPressure() {
        return bloodPressure;
    }

    public void setBloodPressure(double bloodPressure) {
        this.bloodPressure = bloodPressure;
    }

    public double getWeightInKilos() {
        return weightInKilos;
    }

    public void setWeightInKilos(double weightInKilos) {
        this.weightInKilos = weightInKilos;
    }

    public double getWeightInPounds() {
        return weightInPounds;
    }

    public void setWeightInPounds(double weightInPounds) {
        this.weightInPounds = weightInPounds;
    }

    public Boolean getIsRecordNormal() {
        return isRecordNormal;
    }

    public void setIsRecordNormal(Boolean isRecordNormal) {
        this.isRecordNormal = isRecordNormal;
    }

}
