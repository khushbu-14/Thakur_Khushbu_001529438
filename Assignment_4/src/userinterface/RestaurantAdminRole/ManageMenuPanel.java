    private void populateDishTable() {
        Menu menuList = restaurant.getMenu();

        DefaultTableModel model = (DefaultTableModel) tblMenuList.getModel();

        int count = 1;

        model.setRowCount(0);
        for (Dish dish : menuList.getMenuList()) {
            Object[] row = new Object[3];
            row[0] = "" + count++;
            row[1] = dish;
            row[2] = dish.getPrice();

            model.addRow(row);

        }
    }
